package com.swcguild.vendingmvc.model;

/**
 *
 * @author apprentice
 */
public class Item {
        public int itemId;
    
    public String name;
    public double cost;
    
    public Item(int itemId, String name, double cost){
        this.itemId = itemId;
        this.name = name;
        this.cost = cost;
}

    /**
     * @return the itemId
     */
    public int getItemId() {
        return itemId;
    }

    /**
     * @param itemId the itemId to set
     */
    public void setItemId(int itemId) {
        this.itemId = itemId;
    }

    /**
     * @return the name
     */
    public String getName() {
        return name;
    }

    /**
     * @param name the name to set
     */
    public void setName(String name) {
        this.name = name;
    }

    /**
     * @return the cost
     */
    public double getCost() {
        return cost;
    }

    /**
     * @param cost the cost to set
     */
    public void setCost(double cost) {
        this.cost = cost;
    }
    
    
    
}

